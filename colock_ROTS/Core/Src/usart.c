void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)//回调函数  //总的中断回调函数
{
	//HAL_GPIO_WritePin(GPIOA,GPIO_PIN_10,GPIO_PIN_SET);//在这里测试是否接收到数据
	
	if (huart == &huart1)//串口1处理
    {
				//HAL_GPIO_WritePin(GPIOA,GPIO_PIN_11,GPIO_PIN_SET);//测试
        usart1.buf[usart1.length] = pData1[0];//数据接收
  
        if (usart1.length < USART_MAX_RXLENGTH)
        {
            usart1.length++;
        }
        
        usart1.time = 0;
        HAL_UART_Receive_IT(&huart1,pData1,1);//开启中断
    }
		
		if (huart == &huart2)//串口2处理
    {
//        usart2.buf[usart2.length] = pData2[0];//数据接收
//        if (usart2.length < USART_MAX_RXLENGTH)
//        {
//            usart2.length++;
//        }
        //改为接收GPS消息
			
				u8 Res;
				Res =pData2[0];//(USART3->DR);	//读取接收到的数据
					
				if(Res == '$')
				{
					usart2.length = 0;	
				}
					
				usart2.buf[usart2.length++] = Res;

				if(usart2.buf[0] == '$' && usart2.buf[4] == 'M' && usart2.buf[5] == 'C')			//确定是否收到"GPRMC/GNRMC"这一帧数据
				{
					if(Res == '\n')									   
					{
						memset(Save_Data.GPS_Buffer, 0, GPS_Buffer_Length);      //清空
						memcpy(Save_Data.GPS_Buffer, usart2.buf, usart2.length); 	//保存数据
						Save_Data.isGetData = true;
						usart2.length = 0;
						memset(usart2.buf, 0, USART_REC_LEN);      //清空				
					}
				}
				if(usart2.length >= USART_REC_LEN)
				{
					usart2.length = USART_REC_LEN;
				}
			
			
        //usart2.time = 0;
        HAL_UART_Receive_IT(&huart2,pData2,1);//开启中断
    }
	
		if (huart == &huart3)//串口3处理
    {
				//HAL_GPIO_WritePin(GPIOA,GPIO_PIN_10,GPIO_PIN_SET);//测试
        usart3.buf[usart3.length] = pData3[0];//数据接收
  
        if (usart3.length < USART_MAX_RXLENGTH)
        {
            usart3.length++;
        }
        
        usart3.time = 0;
        HAL_UART_Receive_IT(&huart3,pData3,1);//开启中断
    }
}

